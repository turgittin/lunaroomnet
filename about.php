            <p><em><strong>¤&nbsp;</strong></em><strong>Biography | Özgeçmiş:</strong></p>
            <p>1984 İstanbul doğumlu. Saint Joseph  Fransız Lisesi 2002 mezunu. 2006'da Marmara Üniversitesi Güzel Sanatlar  Fakültesi Sinema - Tv bölümünü birincilikle bitirdi. 2008'de New York Film  Academy (Paris - La Femis) filmmaking workshop'una katıldı. 2009-2010  yıllarında Kadir Has Üniversitesi'nde Müzik Video Yapımı ve Fotoğraf Sanatı  derslerini verdi. 2013&rsquo;te Kadir Has Üniversitesi Film ve Drama bölümünde yüksek  lisansını tamamladı. SAE Institute İstanbul&rsquo;da Müzik Video Yapımı ve Akademi İstanbul&rsquo;da  Sinematografi derslerini vermekte. Beş kısa filmi var. Profesyonel alanda  yönetmenlik ve fotoğrafçılık yapıyor.</p>
            <p>Born in 1984 in Istanbul. Graduated  from Saint Joseph French College in 2002 and Marmara University Fine Arts  Faculty, Cinema Tv department in 2006. Participated in filmmaking workshop at  New York Film Academy - Paris La Femis in 2008. She was lecturer of Music Video  Production and Photography at Kadir Has University in 2009-2010. Made her MFA  at Film and Drama program at Kadir Has University. Currently she is a lecturer  for Music Video Production at SAE Institute Istanbul and for Cinematography at  Akademi Istanbul. She has five short films. Works as a professional director  and photographer.</p>
